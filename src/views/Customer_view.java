    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        try {
            String fName = FirstNameTextField.getText().isEmpty() ? Customer.getFirstName() : FirstNameTextField.getText();
            String lName = LastNameLabel.getText().isEmpty() ? Customer.getFirstName() : LastNameLabel.getText();
            String phone = PhoneNumberTextField.getText().isEmpty() ? Customer.getFirstName() : PhoneNumberTextField.getText();
            String email = EmailTextField.getText().isEmpty() ? Customer.getFirstName() : EmailTextField.getText();
            String password = PasswordField.getText().isEmpty() ? Customer.getFirstName() : PasswordField.getText();
            
            Customer.updateCustomer(fName, lName, email, phone, Customer.getUsername(), password);
            
        } catch (SQLException ex) {
            ex.printStackTrace();
            Bank.showError("SQL error");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void transferButtonLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transferButtonLoginButtonActionPerformed
        try{

            int transferVal = Integer.parseInt(transferAmount.getText());
            int transferAccNum = Integer.parseInt(transferAccountNumber.getText());
            int balance = 0;
            balance = Customer.getBalance(Bank.selectedAccountNumber);
            System.out.println(balance+ " " + transferVal + " " + transferAccNum + " " +Bank.selectedAccountNumber);
            if(transferVal<10){
                Bank.showError("Requested Transfer amount is less than minimun value.");
                return;
            } else if(transferVal>balance){
                Bank.showError("Requested Transfer amount is more than your current balance.");
                return;
            }

            Customer.Transfer(transferVal, Bank.selectedAccountNumber, transferAccNum);
            Bank.showError("Please fill both field before submitting");
        }catch(Exception ex)
        {
            Bank.showError("Internal Error");
        }
    }//GEN-LAST:event_transferButtonLoginButtonActionPerformed

