    void selectedAccNum(){
        try {
            Bank.selectedAccountNumber = Integer.parseInt((String) selectAccount.getSelectedItem());
            selectAccount.removeAllItems();
            selectAccount.addItem("------");
            accounts = getAccounts().toArray();
            for(int i=0;i<accounts.length;i++)
            {
                selectAccount.addItem((String) accounts[i]);
                
            }
            selectAccount.setSelectedItem(Bank.selectedAccountNumber);
            selectedAccountLabel.setText(String.valueOf(Bank.selectedAccountNumber));
            NumberFormat format = NumberFormat.getCurrencyInstance();
            balanceLabel.setText(format.format(Customer.getBalance(Bank.selectedAccountNumber)));
            Connection conn = MySql.getConnection();
            PreparedStatement ps = conn.prepareStatement("Select transaction_id as ID, Sender as 'Account Number', amount as 'Amount', time as Time from Transaction where sender = ? or recipient = ? ORDER BY time DESC limit 5");
            ps.setInt(1, Bank.selectedAccountNumber);
            ps.setInt(2, Bank.selectedAccountNumber);
            ResultSet rs = ps.executeQuery();
            resultSetToTableModel(rs, summaryReport);
//            currentBalance.setText(Integer.toString(Bank.selectedAccountNumber));
        } catch (SQLException ex) {
            Logger.getLogger(Customer_view.class.getName()).log(Level.SEVERE, null, ex);
        } catch (java.lang.NumberFormatException ex){
            
        }
    }
